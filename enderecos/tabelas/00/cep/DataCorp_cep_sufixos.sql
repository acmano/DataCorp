  DECLARE @mensagemP2 varchar(100);
  DECLARE @mensagemP3 varchar(100);
  DECLARE @mensagemP4 varchar(100);
  
  SET @erro = 1
  SET @mensagemP2 = 'ERRO! '
  SET @mensagemP3 = 'DROP  : '
  SET @mensagemP4 = 'Tabela      [DataCorp].[cep].[sufixos] >>>'
  IF EXISTS ( SELECT  DISTINCT 1  FROM  sys.tables tabela INNER JOIN sys.schemas esquema ON  esquema.schema_id = tabela.schema_id WHERE esquema.name = 'cep' AND tabela.name = 'sufixos' )
  BEGIN
    DROP TABLE [cep].[sufixos]
  END

  SET @mensagemP2 = 'Ok!  '
  PRINT @mensagemP1 + @mensagemP2 + @mensagemP3 + @mensagemP4


  SET @erro = 2
  SET @mensagemP2 = 'ERRO! '
  SET @mensagemP3 = 'CREATE: '
  SET @mensagemP4 = 'Tabela      [DataCorp].[cep].[sufixos] >>>'
  CREATE TABLE [cep].[sufixos]
  (

    [sufId]
      BIGINT
      IDENTITY(1,1)
      CONSTRAINT [sufixossufIdNn] NOT NULL

  , [codigo]
      VARCHAR(3)
      CONSTRAINT [sufixosCodigoNn] NOT NULL

  , [descricao]
      VARCHAR(128)
      CONSTRAINT [sufixosDescricaoNn] NOT NULL

  )
  SET @mensagemP2 = 'Ok!  '
  PRINT @mensagemP1 + @mensagemP2 + @mensagemP3 + @mensagemP4


