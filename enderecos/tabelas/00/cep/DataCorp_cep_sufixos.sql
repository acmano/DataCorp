  SET @mensagemP4 = 'Primary Key [DataCorp].[cep].[sufixos].[sufPk] >>>'
  ALTER TABLE [cep].[sufixos]
    ADD CONSTRAINT [sufPk]
      PRIMARY KEY CLUSTERED
      (
        [sufId]
      )
  SET @mensagemP2 = 'Ok!  '
  PRINT @mensagemP1 + @mensagemP2 + @mensagemP3 + @mensagemP4


  SET @erro = 4
  SET @mensagemP2 = 'ERRO! '
  SET @mensagemP3 = 'DROP  : '
  SET @mensagemP4 = 'Index       [DataCorp].[cep].[sufixos].[sufIxAk] >>>'
  IF EXISTS (SELECT * FROM sys.indexes WHERE object_id=OBJECT_ID('cep.sufixos') AND name='sufIxAk')
  BEGIN
    DROP INDEX [cep].[sufixos].[sufIxAk]
    SET @mensagemP2 = 'Ok!  '
    PRINT @mensagemP1 + @mensagemP2 + @mensagemP3 + @mensagemP4
  END

  SET @erro = 5
  SET @mensagemP2 = 'ERRO!  '
  SET @mensagemP3 = 'CREATE: '
  SET @mensagemP4 = 'Index       [DataCorp].[cep].[sufixos].[sufIxAk] >>>'
  CREATE UNIQUE INDEX [sufIxAk]
