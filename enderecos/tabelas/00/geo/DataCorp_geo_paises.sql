  SET @mensagemP4 = 'Tabela      [DataCorp].[geo].[paises] >>>'
  CREATE TABLE [geo].[paises]
  (

    [paiId]
      BIGINT
      IDENTITY(1,1)
      CONSTRAINT [paisesPaiIdNn] NOT NULL

