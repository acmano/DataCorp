  SET @mensagemP4 = 'Index       [DataCorp].[cal].[quinzenas].[quiIxAk] >>>'
  CREATE UNIQUE INDEX [quiIxAk]
    ON [cal].[quinzenas]
    (
      [nome]
    )
  SET @mensagemP2 = 'Ok!  '
  PRINT @mensagemP1 + @mensagemP2 + @mensagemP3 + @mensagemP4

