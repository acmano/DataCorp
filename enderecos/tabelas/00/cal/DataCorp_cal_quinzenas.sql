  DECLARE @mensagemP1 varchar(100) = '<<< ';
  DECLARE @mensagemP2 varchar(100);
  DECLARE @mensagemP3 varchar(100);
  DECLARE @mensagemP4 varchar(100);
  
  SET @erro = 1
  SET @mensagemP2 = 'ERRO! '
  SET @mensagemP3 = 'DROP  : '
  SET @mensagemP4 = 'Tabela      [DataCorp].[cal].[quinzenas] >>>'
  IF EXISTS ( SELECT  DISTINCT 1  FROM  sys.tables tabela INNER JOIN sys.schemas esquema ON  esquema.schema_id = tabela.schema_id WHERE esquema.name = 'cal' AND tabela.name = 'quinzenas' )
  BEGIN
    DROP TABLE [cal].[quinzenas]
  END

  SET @mensagemP2 = 'Ok!  '
  PRINT @mensagemP1 + @mensagemP2 + @mensagemP3 + @mensagemP4


  SET @erro = 2
  SET @mensagemP2 = 'ERRO! '
  SET @mensagemP3 = 'CREATE: '
  SET @mensagemP4 = 'Tabela      [DataCorp].[cal].[quinzenas] >>>'
  CREATE TABLE [cal].[quinzenas]
  (

    [quiId]
      BIGINT
      IDENTITY(1,1)
      CONSTRAINT [quinzenasquiIdNn] NOT NULL

  , [nome]
      VARCHAR(64)
      CONSTRAINT [quinzenasNomeNn] NOT NULL

  , [descricao]
      VARCHAR(128)
      CONSTRAINT [quinzenasDescricaoNn] NOT NULL

  )
  SET @mensagemP2 = 'Ok!  '
  PRINT @mensagemP1 + @mensagemP2 + @mensagemP3 + @mensagemP4


  SET @erro = 3
  SET @mensagemP2 = 'ERRO! '
  SET @mensagemP3 = 'CREATE: '
  SET @mensagemP4 = 'Primary Key [DataCorp].[cal].[quinzenas].[quiPk] >>>'
  ALTER TABLE [cal].[quinzenas]
    ADD CONSTRAINT [quiPk]
      PRIMARY KEY CLUSTERED
      (
        [quiId]
      )
  SET @mensagemP2 = 'Ok!  '
  PRINT @mensagemP1 + @mensagemP2 + @mensagemP3 + @mensagemP4


